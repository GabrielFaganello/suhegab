        function startCountdown(){

          var date = new Date();
          var set_day = 7;
          var set_month = 6;
          var set_year = 2018;
          
          var msecPerMinute = 1000 * 60;
          var msecPerHour = msecPerMinute * 60;
          var msecPerDay = msecPerHour * 24;

          var dateMsec = date.getTime();


          if (date.getDate() == set_day && date.getMonth() == set_month && date.getFullYear() == set_year) {

            $("#other_day").addClass("hide");
            $("#theme_other").addClass("hide");
            $("#today").removeClass("hide");
            $("#time_today").html("É HOJE!");

          }else{

            date.setFullYear(set_year);
            date.setMonth(set_month);
            date.setDate(set_day);
            date.setHours(0);
            date.setMinutes(0);
            date.setSeconds(0);

            if (date.getTime() > dateMsec) {

                var interval =date.getTime() - dateMsec;

                var days = Math.floor(interval / msecPerDay );
                interval = interval - (days * msecPerDay );

                var hours = Math.floor(interval / msecPerHour );
                interval = interval - (hours * msecPerHour );

                var minutes = Math.floor(interval / msecPerMinute );
                interval = interval - (minutes * msecPerMinute );

                var seconds = Math.floor(interval / 1000 );
                
                $("#time_day").html(days);
                $("#time_hour").html(hours);
                $("#time_minute").html(minutes);
                $("#time_second").html(seconds);
                
                setTimeout('startCountdown()',1000);

            }else{

              $("#other_day").addClass("hide");
              $("#theme_other").addClass("hide");
              $("#today").removeClass("hide");

              $("#time_today").html("Melhor dia! 07/07/2018");

            }

          }

        }

